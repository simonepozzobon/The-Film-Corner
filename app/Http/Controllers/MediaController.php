    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $media = Media::findOrFail($id);
        $file = $media->url;
        Storage::delete($file);
        $media->delete();
        return redirect('/admin/media')->with('status', 'Media deleted!');
    }
